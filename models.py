class Property():
    def __init__(self, id, name, summary, address, price, cleaning_fee, guests, image, amenities):
        self.id = id
        self.name = name
        self.summary = summary
        self.address = address
        self.price = price
        self.cleaning_fee = cleaning_fee
        self.guests = guests
        self.image = image
        self.amenities = amenities